//Type of  unary operator increment and decreament operator

var a=2
a++            //postfix
console.log(a)

var b=2
++b            //prefix
console.log(b)


var c=2
c--            //postfix
console.log(c)

var d=2
--d           //prefix
console.log(d)

console.log('2'=="2") // == force to change in one form
console.log('2'==="2")  //can stop type coercion